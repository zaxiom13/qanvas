#!/usr/bin/env node
import { spawnSync } from "node:child_process";
import { existsSync, mkdirSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join, resolve } from "node:path";

const USER_AGENT = "qanvas-regression-bot/0.1";
const CATEGORY_API =
  "https://rosettacode.org/w/api.php?action=query&list=categorymembers&cmtitle=Category:Q&cmlimit=500&format=json";
const Q_BIN = process.env.QANVAS_Q_BIN || `${process.env.HOME}/.kx/bin/q`;
const OUT_PATH = resolve("packages/q-engine/tests/fixtures/rosetta-q-corpus.ts");

const decodeEntities = (text) =>
  text
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&amp;/g, "&")
    .replace(/&quot;/g, "\"")
    .replace(/&#039;/g, "'");

const stripPrompts = (code) =>
  code
    .split(/\r?\n/)
    .map((line) => line.replace(/^\s*q\)\s?/, "").replace(/^\s*>\s?/, ""))
    .join("\n")
    .trim();

function extractQBlocks(raw, task) {
  const lines = raw.split(/\r?\n/);
  const blocks = [];
  let inQ = false;
  let buffer = null;

  for (const line of lines) {
    const header = line.match(/^={2,}\s*\{\{header\|([^}|]+)[^}]*\}\}/i);
    if (header) {
      inQ = header[1].trim().toLowerCase() === "q";
    }
    if (!inQ) continue;

    if (buffer === null) {
      const start =
        line.match(/<syntaxhighlight\b([^>]*)>(.*)$/i) ||
        line.match(/<lang\b([^>]*)>(.*)$/i);
      if (!start || !/(?:lang\s*=\s*['"]?q\b|\bq\b)/i.test(start[1])) {
        continue;
      }

      buffer = [];
      const end = start[2].match(/(.*?)<\/syntaxhighlight>|(.*?)<\/lang>/i);
      if (end) {
        blocks.push(stripPrompts(decodeEntities(end[1] ?? end[2] ?? "")));
        buffer = null;
      } else {
        buffer.push(start[2]);
      }
      continue;
    }

    const end = line.match(/(.*?)<\/syntaxhighlight>|(.*?)<\/lang>/i);
    if (end) {
      buffer.push(end[1] ?? end[2] ?? "");
      blocks.push(stripPrompts(decodeEntities(buffer.join("\n"))));
      buffer = null;
    } else {
      buffer.push(line);
    }
  }

  return blocks
    .filter(Boolean)
    .map((source, index) => ({
      task,
      url: `https://rosettacode.org/wiki/${encodeURIComponent(task.replaceAll(" ", "_"))}#q`,
      block: index + 1,
      source
    }));
}

const qString = (source) =>
  `"${source.replaceAll("\\", "\\\\").replaceAll("\"", "\\\"").replaceAll("\n", "\\n")}"`;

function realQParseOk(source) {
  if (!existsSync(Q_BIN)) return true;

  const dir = mkdtempSync(join(tmpdir(), "qanvas-rosetta-"));
  const outPath = join(dir, "out.txt");
  const scriptPath = join(dir, "runner.q");

  try {
    writeFileSync(
      scriptPath,
      [
        `ok:@[{parse x;1b};${qString(source)};{0b}]`,
        `(\`$":${outPath}") 0: enlist string ok`,
        "\\\\"
      ].join("\n"),
      "utf8"
    );
    const result = spawnSync(Q_BIN, [scriptPath], {
      encoding: "utf8",
      env: { ...process.env, QHOME: process.env.QHOME || `${process.env.HOME}/.kx` },
      timeout: 10000
    });
    if (result.status !== 0) return false;
    return readFileSync(outPath, "utf8").trim() === "1";
  } finally {
    rmSync(dir, { recursive: true, force: true });
  }
}

async function fetchJson(url) {
  const response = await fetch(url, { headers: { "user-agent": USER_AGENT } });
  if (!response.ok) throw new Error(`HTTP ${response.status} for ${url}`);
  return response.json();
}

async function fetchText(url) {
  const response = await fetch(url, { headers: { "user-agent": USER_AGENT } });
  if (!response.ok) throw new Error(`HTTP ${response.status} for ${url}`);
  return response.text();
}

const category = await fetchJson(CATEGORY_API);
const pages = category.query.categorymembers
  .filter((page) => page.ns === 0)
  .map((page) => page.title);

const examples = [];
for (const page of pages) {
  const raw = await fetchText(
    `https://rosettacode.org/wiki/${encodeURIComponent(page.replaceAll(" ", "_"))}?action=raw`
  );
  examples.push(...extractQBlocks(raw, page));
}

const enriched = examples.map((example) => ({
  ...example,
  realQParse: realQParseOk(example.source)
}));

const body = `// Generated by scripts/update-rosetta-q-corpus.mjs from Rosetta Code Category:Q.
// Do not edit entries manually; refresh the corpus with the script above.

export interface RosettaQExample {
  task: string;
  url: string;
  block: number;
  realQParse: boolean;
  source: string;
}

export const ROSETTA_Q_CORPUS: RosettaQExample[] = ${JSON.stringify(enriched, null, 2)};
`;

mkdirSync(dirname(OUT_PATH), { recursive: true });
writeFileSync(OUT_PATH, body, "utf8");
console.log(`Wrote ${enriched.length} q examples to ${OUT_PATH}`);
